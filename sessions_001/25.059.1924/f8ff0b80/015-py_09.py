import numpy as np

def describe_grid(grid):
    """Provides a basic description of a grid."""
    grid = np.array(grid)
    print(f"  Shape: {grid.shape}")
    print(f"  Unique Values: {np.unique(grid)}")
    # Count of each color:
    print("  Color Counts:")
    for color in np.unique(grid):
        count = np.sum(grid == color)
        print(f"    {color}: {count}")

