import numpy as np

def describe_grid(grid):
    grid = np.array(grid)
    unique, counts = np.unique(grid, return_counts=True)
    color_counts = dict(zip(unique, counts))
    height, width = grid.shape
    description = {
        'shape': (height, width),
        'colors': color_counts,
    }
    return description

