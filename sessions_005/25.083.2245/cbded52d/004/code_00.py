def find_mismatched_pixels(expected, transformed):
    mismatches = []
    for r in range(len(expected)):
        for c in range(len(expected[0])):
            if expected[r][c] != transformed[r][c]:
                mismatches.append((r, c, expected[r][c], transformed[r][c]))
    return mismatches

