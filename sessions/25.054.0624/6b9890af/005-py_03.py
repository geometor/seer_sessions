def find_colored_pixels(grid):
    """Finds all pixels with non-zero color."""
    colored_pixels = []
    for i in range(grid.shape[0]):
        for j in range(grid.shape[1]):
            if grid[i, j] != 0:
                colored_pixels.append((i, j, grid[i, j]))
    return colored_pixels

