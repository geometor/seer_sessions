import numpy as np

def compare_grids(grid1, grid2):
    grid1 = np.array(grid1)
    grid2 = np.array(grid2)
    if grid1.shape != grid2.shape:
        return "Shapes differ"
    diff = grid1 != grid2
    return np.sum(diff)

