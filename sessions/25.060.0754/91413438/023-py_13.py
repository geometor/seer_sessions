import numpy as np

def get_grid_info(grid):
    height, width = grid.shape
    unique_values = np.unique(grid)
    num_unique_values = len(unique_values)
    return {
        "height": height,
        "width": width,
        "unique_values": unique_values.tolist(),
        "num_unique_values": num_unique_values,
    }

