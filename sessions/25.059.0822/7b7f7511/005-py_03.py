def grid_report(grid):
    """Provides a concise report on a grid."""
    np_grid = np.array(grid)
    height, width = np_grid.shape
    unique_colors = np.unique(np_grid)
    return f"Size: {width}x{height}, Colors: {list(unique_colors)}"

