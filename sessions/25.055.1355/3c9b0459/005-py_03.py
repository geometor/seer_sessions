def compare_grids(grid1, grid2):
    import numpy as np
    np_grid1 = np.array(grid1)
    np_grid2 = np.array(grid2)
    if np_grid1.shape != np_grid2.shape:
        return False
    else:
        return (np_grid1 == np_grid2).all()


